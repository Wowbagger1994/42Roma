char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	while (*(s + i) != '\0')
	{
		if (*(s + i) == (char)c)
			break ;
		i++;
	}
	if (*(s + i) != '\0' || c == 0)
		return (s + i);
	return (0);
}
