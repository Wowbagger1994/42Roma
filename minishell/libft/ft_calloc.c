#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*buffer;

	buffer = malloc(size * count);
	if (buffer == NULL)
		return (NULL);
	ft_bzero(buffer, size * count);
	return (buffer);
}
